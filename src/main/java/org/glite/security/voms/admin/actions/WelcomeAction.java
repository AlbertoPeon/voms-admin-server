package org.glite.security.voms.admin.actions;

import java.util.Date;
import java.util.List;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;
import org.glite.security.voms.admin.dao.VOMSUserDAO;


public class WelcomeAction extends BaseAction {

    /*
     * (non-Javadoc)
     * 
     * @see org.apache.struts.action.Action#execute(org.apache.struts.action.ActionMapping,
     *      org.apache.struts.action.ActionForm,
     *      javax.servlet.http.HttpServletRequest,
     *      javax.servlet.http.HttpServletResponse)
     */
    public ActionForward execute( ActionMapping mapping, ActionForm form,
            HttpServletRequest req, HttpServletResponse res ) throws Exception {

        try {

            req.getSession().setAttribute( "startDate", new Date() );

            List l = VOMSUserDAO.instance().getAll();
            req.setAttribute( "users", l );

        } catch ( Exception e ) {
            log.error( "Error!", e );
            req.setAttribute( "exception", e );
            super.findFailure( mapping );
        }

        return super.findSuccess( mapping );
    }

}
